class Elementales:    
    def suma(self, numero1: int, numero2: int) -> int:
        return numero1 + numero2

    def resta(self, numero1: int, numero2: int) -> int:
        return numero1 - numero2

    def multiplicacion(self, numero1: int, numero2: int) -> int:
        return numero1 * numero2

    def division(self, numero1: int, numero2: int) -> float:
        return numero1 / numero2
    
